#include "gradido_blockchain_core/interactions/validate/result_type.h"

const char *grdi_validate_result_to_string(grdi_validate_result_type result) {
  static const char *messages[] = {
      [GRDI_VALIDATE_SUCCESS] = "GRDI_VALIDATE_SUCCESS",
      [GRDI_VALIDATE_NULL_POINTER] = "GRDI_VALIDATE_NULL_POINTER",
      [GRDI_VALIDATE_INVALID_ERROR_DETAILS_STATE] = "GRDI_VALIDATE_INVALID_ERROR_DETAILS_STATE",
      [GRDI_VALIDATE_INVALID_ADDRESS_TYPE] = "GRDI_VALIDATE_INVALID_ADDRESS_TYPE",
      [GRDI_VALIDATE_INVALID_FIELD] = "GRDI_VALIDATE_INVALID_FIELD",
      [GRDI_VALIDATE_ENUM_UNHANDLED] = "GRDI_VALIDATE_ENUM_UNHANDLED",
      [GRDI_VALIDATE_INVALID_TRANSACTION_TYPE] = "GRDI_VALIDATE_INVALID_TRANSACTION_TYPE",
      [GRDI_VALIDATE_FIELD_VALUE_CONFLICT] = "GRDI_VALIDATE_FIELD_VALUE_CONFLICT",
      [GRDI_VALIDATE_MISSING_SIGN] = "GRDI_VALIDATE_MISSING_SIGN",
      [GRDI_VALIDATE_CONTRIBUTION_SUM_EXCEEDED] = "GRDI_VALIDATE_CONTRIBUTION_SUM_EXCEEDED",
      [GRDI_VALIDATE_CONTRIBUTION_INVALID_TARGET_DATE] =
          "GRDI_VALIDATE_CONTRIBUTION_INVALID_TARGET_DATE",
      [GRDI_VALIDATE_CRYPTO_SIGN_INVALID] = "GRDI_VALIDATE_CRYPTO_SIGN_INVALID",
      [GRDI_VALIDATE_NOT_IMPLEMENTED_YET] = "GRDI_VALIDATE_NOT_IMPLEMENTED_YET",
      [GRDI_VALIDATE_WRONG_ERROR_DETAILS_USAGE] = "GRDI_VALIDATE_WRONG_ERROR_DETAILS_USAGE",
  };

  if (result < 0 || result >= (int)(sizeof(messages) / sizeof(messages[0]))) {
    return "GRDI_VALIDATE_UNKNOWN";
  }

  const char *msg = messages[result];
  return msg ? msg : "GRDI_VALIDATE_UNKNOWN";
}

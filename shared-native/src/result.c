#include "gradido_blockchain_core/result.h"

const char *grd_result_to_string(grd_result result) {
  static const char *messages[] = {
      [GRD_SUCCESS] = "GRD_SUCCESS",
      [GRD_WARNING_USED_DYNAMIC_ALLOCATION_FALLBACK] =
          "GRD_WARNING_USED_DYNAMIC_ALLOCATION_FALLBACK",
      [GRD_ERROR_NOT_IMPLEMENTED_YET] = "GRD_ERROR_NOT_IMPLEMENTED_YET",
      [GRD_ERROR_NOT_INITIALIZED] = "GRD_ERROR_NOT_INITIALIZED",
      [GRD_ERROR_INVALID_PARAM] = "GRD_ERROR_INVALID_PARAM",
      [GRD_ERROR_INVALID_ENUM_TYPE] = "GRD_ERROR_INVALID_ENUM_TYPE",
      [GRD_ERROR_INVALID_STATE] = "GRD_ERROR_INVALID_STATE",
      [GRD_ERROR_NULL_POINTER] = "GRD_ERROR_NULL_POINTER",
      [GRD_ERROR_ARITHMETIC_OVERFLOW] = "GRD_ERROR_ARITHMETIC_OVERFLOW",
      [GRD_ERROR_OUT_OF_MEMORY] = "GRD_ERROR_OUT_OF_MEMORY",
      [GRD_ERROR_ARRAY_INDEX_OUT_OF_BOUNDS] = "GRD_ERROR_ARRAY_INDEX_OUT_OF_BOUNDS",
      [GRD_ERROR_DECODE_FAILED] = "GRD_ERROR_DECODE_FAILED",
      [GRD_ERROR_ENCODE_FAILED] = "GRD_ERROR_ENCODE_FAILED",
      [GRD_ERROR_DESTINATION_BUFFER_TO_SMALL] = "GRD_ERROR_DESTINATION_BUFFER_TO_SMALL",
      [GRD_ERROR_ENUM_UNHANDLED] = "GRD_ERROR_ENUM_UNHANDLED",
      [GRD_ERROR_ENUM_UNKNOWN] = "GRD_ERROR_ENUM_UNKNOWN",
      [GRD_ERROR_PB_UNHANDLED_ONEOF_BRANCH] = "GRD_ERROR_PB_UNHANDLED_ONEOF_BRANCH",
      [GRD_ERROR_PB_UNHANDLED_PARAMETER] = "GRD_ERROR_PB_UNHANDLED_PARAMETER",
      [GRD_ERROR_PB_INCORRECT_VERSION] = "GRD_ERROR_PB_INCORRECT_VERSION",
  };

  if (result < 0 || result >= (int)(sizeof(messages) / sizeof(messages[0]))) {
    return "GRD_ERROR_UNKNOWN";
  }
  const char *msg = messages[result];
  return msg ? msg : "GRD_ERROR_UNKNOWN";
}
